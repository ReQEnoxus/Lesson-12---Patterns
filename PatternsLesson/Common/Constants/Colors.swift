import Foundation
import UIKit

extension UIColor {

    @nonobjc class var blackBg: UIColor {
      return UIColor(white: 30.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var orangeUi: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 159.0 / 255.0, blue: 10.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var paleGrey: UIColor {
      return UIColor(red: 235.0 / 255.0, green: 235.0 / 255.0, blue: 245.0 / 255.0, alpha: 1)
    }
}
